package com.learn.basics;

public class WhileLoopEx {
	public static void main(String[] args) {
		int i = 1;
		while(i <= 5) {
			System.out.println("i value is - "+ i);
			i++;
		}
		
		System.out.println("--------------");
		
		int j = 10;
		while (j >= 1) {
			System.out.println("j value is - "+ j);
			j--;
		}
	}
}
