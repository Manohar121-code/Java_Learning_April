package com.learn.basics;

public class MultipleIfConditionEx {
	public static void main(String[] args) {
		int i = 10;
		if (i > 10) {
			System.out.println("i value is > 10");
		} else if (i < 10) {
			System.out.println("i value is < 10");
		} else {
			System.out.println("i is equals to 10");
		}
	}
}
